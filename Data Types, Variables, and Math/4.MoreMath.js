//Old Method of Math
{
    let a = 3;
    a = a + 4;
    console.log(a);
    a = a - 4
    console.log(a);
    a = a * 2
    console.log(a);
    a = a / 2
    console.log(a);
}

//Shortcut Method of Math
{
    let a = 3;
    a += 4;
    console.log(a);
    a -= 4;
    console.log(a);
    a *= 2
    console.log(a)
    a /= 2
    console.log(a);
}